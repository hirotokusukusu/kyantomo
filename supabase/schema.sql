-- ==================
-- Optional helper RPC: attach hashtags to a post in one call
-- ==================
-- Client can pass an array of tag strings; server upserts hashtags and creates joins.
-- This avoids race conditions and simplifies client code.
create or replace function public.set_post_hashtags(p_post_id uuid, p_tags text[])
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  t text;
  h_id bigint;
begin
  -- Ensure caller owns the post
  if not exists (select 1 from public.posts where id = p_post_id and author_id = auth.uid()) then
    raise exception 'not allowed';
  end if;

  -- Clear existing
  delete from public.post_hashtags where post_id = p_post_id;

  -- Insert new
  if p_tags is null then
    return;
  end if;

  foreach t in array p_tags loop
    t := btrim(t);
    if t = '' then
      continue;
    end if;

    insert into public.hashtags(name)
    values (t)
    on conflict (lower(name)) do update set name = excluded.name
    returning id into h_id;

    insert into public.post_hashtags(post_id, hashtag_id)
    values (p_post_id, h_id)
    on conflict do nothing;
  end loop;
end;
$$;

-- Allow authenticated users to call the function
revoke all on function public.set_post_hashtags(uuid, text[]) from public;
grant execute on function public.set_post_hashtags(uuid, text[]) to authenticated;
